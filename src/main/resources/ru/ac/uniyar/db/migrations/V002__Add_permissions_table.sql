CREATE TABLE PERMISSIONS (
    ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR(50) NOT NULL,
    CAN_BECOME_TRUSTEE BIT NOT NULL,
    CAN_WATCH_ENTRY_BY_TRUSTEE BIT NOT NULL,
    CAN_WATCH_TRUSTED_ANIMAL BIT NOT NULL,
    CAN_WATCH_PERSONAL_TAPE BIT NOT NULL,
    CAN_ADD_ANIMAL BIT NOT NULL,
    CAN_ADD_ENTRY_ANIMAL BIT NOT NULL,
    CAN_WATCH_TRUSTEE_BY_ANIMAL BIT NOT NULL,
    CAN_ADD_ENTRY_FOR_TRUSTEE BIT NOT NULL
);